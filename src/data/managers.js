// 二级分类 → 一级分类 的映射
export const subCategoryToGroup = Object.entries(strategyTaxonomy).reduce(
  (acc, [group, subs]) => {
    subs.forEach((sub) => {
      acc[sub] = group
    })
    return acc
  },
  {}
)

// 根据二级分类获取其所属的一级分类
export function getStrategyGroup(category) {
  return subCategoryToGroup[category] || '其他'
}
